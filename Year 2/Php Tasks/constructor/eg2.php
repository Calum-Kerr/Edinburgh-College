<?php
class Car {
  public $name;
  public $colour;

  function __construct($name, $colour) {
    $this->name = $name;
    $this->colour = $colour;
  }
  function get_name() {
    return $this->name;
  }
  function get_colour() {
    return $this->colour;
  }
}

$audi = new Car("Audi", "red");
echo $audi->get_name();
echo "<br>";
echo $audi->get_colour();
?>